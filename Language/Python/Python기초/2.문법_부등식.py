# 파이썬의 부등식은 아래와 같다.

# 대부분의 언어와 유사하다.
print(1<1)
print(1>1)
print(1>=1)
print(1<=1)
print(1==1)
print(1!=1)

# 하지만 파이썬에서는 밑과 같은 부등식이 가능하다.
print(1<2<3)
print(1==2==3)


# 파이썬의 boolean값은 항상 앞 글자가 대문자다.
# 파이썬에서는 &(AND)|(OR)에 대한 연산도 가능하다.(비트 연산이 아님)

print(True|False)
print(True&False)

# 다른 방식의 boolean연산도 가능하다.
print(True and False)
print(True or False)
